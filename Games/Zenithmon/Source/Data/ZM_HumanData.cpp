const ZM_HumanData& ZM_GetHumanData(ZM_HUMAN_ID eId)
{
	Zenith_Assert(eId < ZM_HUMAN_COUNT, "ZM_GetHumanData: human id out of range (%u)", (u_int)eId);
	return s_axHumans[eId];
}

u_int ZM_GetHumanCount()
{
	return ZM_HUMAN_COUNT;
}

const char* ZM_GetHumanName(ZM_HUMAN_ID eId)
{
	if (eId >= ZM_HUMAN_COUNT)
	{
		return "NONE";
	}
	return s_axHumans[eId].m_szName;
}
